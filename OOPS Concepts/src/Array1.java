/**
 * Simple Array Program
 */
public class Array1 {
   public static void main(String[] args)
   {
	   int arr[] = new int[5];
	    arr[0] =5;
	    arr[1] =3;
	    arr[2] =9;
	    arr[3] =1;
	    arr[4] =4;
	    
	    for(int i =0; i<=arr.length;i++)
	    {
	    	System.out.println(arr[i]);
	    }
   }
}
